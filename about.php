Reddit is a social news aggregation, web content rating, and discussion website, and it claims to be "the front-page of the internet" as its moniker, recently including livestream content through Reddit Public Access Network. Registered members submit content to the site such as links, text posts, images, and videos, which are then voted up or down by other members. 
    
Posts are organized by subject into user-created boards called "communities" or "subreddits", which cover a variety of topics such as news, politics, religion, science, movies, video games, music, books, sports, fitness, cooking, pets, and image-sharing. 
    
Submissions with more up-votes appear towards the top of their subreddit and, if they receive enough up-votes, ultimately on the site's front page. Despite strict rules prohibiting harassment, Reddit's administrators have to moderate the communities and, on occasion, close them. Moderation is also conducted by community-specific moderators, who are not considered Reddit employees. 
    
As of February 2021, Reddit ranks as the 18th-most-visited website in the world and 7th most-visited website in the U.S., according to Alexa Internet. About 42-49.3% of its user base comes from the United States, followed by the United Kingdom at 7.9-8.2% and Canada at 5.2-7.8%. 22 percent of U.S. adults aged 18 to 29 years, and 14 percent of U.S. adults aged 30 to 49 years, regularly use Reddit.
    
